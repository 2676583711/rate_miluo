package com.rate.web.statistic.dto;

public class Px implements Comparable<Px> {
    private String name;
    private Double value;

    public Px() {
    }

    public Px(String name, Double value) {
        this.name = name;
        this.value = value;
    }

    @Override
    public int compareTo(Px o) {
        return 0;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getValue() {
        return value;
    }

    public void setValue(Double value) {
        this.value = value;
    }
}
